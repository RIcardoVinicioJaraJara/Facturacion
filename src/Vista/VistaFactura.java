    private void btnGuardarFacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarFacturaActionPerformed

        boolean bandera = true;

        if (usuarioAux == null) {
            bandera = false;
        }
        if (tablaDetalle.getRowCount() <= 0) {
            bandera = false;
        }
        if (bandera) {
            double subtotal = Double.parseDouble(txtSubtotal.getText());
            double iva = Double.parseDouble(txtIVA.getText());;
            double total = Double.parseDouble(txtTotalFactura.getText());;
            String fecha = txtFecha.getText();
            Usuario usuario = usuarioAux;
            Factura factura = new Factura(0, subtotal, iva, total, fecha, usuario);
            if (controladorFactura.ingresar(factura)) {
                for (int i = 0; i < tablaDetalle.getRowCount(); i++) {
                    int cantidad = Integer.parseInt(tablaDetalle.getValueAt(i, 2) + "");
                    double subtotald = Double.parseDouble(tablaDetalle.getValueAt(i, 4) + "");
                    Producto producto = controladorProducto.buscar(tablaDetalle.getValueAt(i, 0) + "");
                    Factura facturad = controladorFactura.buscarID(controladorDetalle.obtenerCodigo() + "");
                    Detalle detalle = new Detalle(0, cantidad, subtotald, producto, facturad);
                    if (controladorDetalle.ingresar(detalle)) {
                        producto.setStock(producto.getStock() - cantidad);
                        controladorProducto.actualizar(producto);
                    } else {
                        JOptionPane.showMessageDialog(this, "Error al Detalle");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Error al guardar");
            }
            limpiarTabla();
        } else {
            JOptionPane.showMessageDialog(this, "Por fabor buscar al cleinte o llenar la tabla de productos");
        }
    }//GEN-LAST:event_btnGuardarFacturaActionPerformed
